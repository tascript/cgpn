<script lang='ts'>
  import { onMount, onDestroy } from 'svelte'
  import { position, isAnimate } from './lib/store'
  import { handleMouseover, handleMousedown, handleMouseUp } from './lib/event'

  export let repeat: number

  onMount(() => {
    window.addEventListener('mousemove', handleMouseover as EventListener)
    window.addEventListener('mousedown', handleMousedown)
    window.addEventListener('mouseup', handleMouseUp)
  })
  onDestroy(() => {
    window.removeEventListener('mousemove', handleMouseover as EventListener)
    window.removeEventListener('mousedown', handleMousedown)
    window.removeEventListener('mouseup', handleMouseUp)
  })
</script>

{#each Array(repeat) as _, i}
<div class="circle--base {$isAnimate === true ? 'circle--animation' : ''} circle--{i}"
	style="transform:translate({$position.x}px,{$position.y}px)"
></div>
{/each}

<style>
	.circle--base {
		--width: 35px;
    --height: 35px;
    border-radius: 50%;
		width: var(--width);
		height: var(--height);
    opacity: 0;
		border: solid thin #4682b4;
    position: absolute;
	}
  .circle--animation {
    animation: hamon 1s;
  }

  @keyframes hamon {
  from {
    width:  0px;
    height: 0px;
    opacity: 1;
  }
  to {
    width:  var(--width);
    height: var(--height);
    opacity: 0 ;
  }
}
</style>
